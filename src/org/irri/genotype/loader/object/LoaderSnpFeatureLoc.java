package org.irri.genotype.loader.object;

public class LoaderSnpFeatureLoc {

	private Integer snpfeatureId;
	private Integer organismId;
	private Integer srcFeatureid;
	private Integer position;
	private String refCall;
	
	public Integer getSnpfeatureId() {
		return snpfeatureId;
	}
	public void setSnpfeatureId(Integer snpfeatureId) {
		this.snpfeatureId = snpfeatureId;
	}
	public Integer getOrganismId() {
		return organismId;
	}
	public void setOrganismId(Integer organismId) {
		this.organismId = organismId;
	}
	public Integer getSrcFeatureid() {
		return srcFeatureid;
	}
	public void setSrcFeatureid(Integer srcFeatureid) {
		this.srcFeatureid = srcFeatureid;
	}
	public Integer getPosition() {
		return position;
	}
	public void setPosition(Integer position) {
		this.position = position;
	}
	public String getRefCall() {
		return refCall;
	}
	public void setRefCall(String refCall) {
		this.refCall = refCall;
	}
	
	
	
	

}
